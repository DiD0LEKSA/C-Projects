#include <iostream>
#include <cmath>

// Основна функція програми
int main() 
{
    // --- Частина 1: Робота з логічними виразами ---
    std::cout << "=== Відповідь №1: Логічні операції ===\n";
    
    // Обчислення булевих виразів (1 - true, 0 - false)
    std::cout << "1. (5 > 3 && 4 < 8) = " << (5 > 3 && 4 < 8) << std::endl;
    std::cout << "2. (4 > 6 && true) = " << (4 > 6 && true) << std::endl; // false, бо перша частина false
    std::cout << "3. (3 >= 3 || false) = " << (3 >= 3 || false) << std::endl;
    
    // Використання тернарного оператора
    std::cout << "4. (true || false) ? 4 : 5 = " << ((true || false) ? 4 : 5) << std::endl;

    std::cout << "\n=== Відповідь №2: Арифметичні операції ===\n";

    // Демонстрація цілочисельної арифметики
    std::cout << "1. 7 / 4 (Цілочисельне ділення) = " << (7 / 4) << std::endl; // Результат 1
    std::cout << "2. 14 % 5 (Залишок від ділення) = " << (14 % 5) << std::endl; // Результат 4

    std::cout << "\n=== Відповідь №3: Конвертація (Двійковий -> Десятковий) ===\n";

    // Переведення 1101 у десяткову систему
    // 1*2^3 + 1*2^2 + 0*2^1 + 1*2^0
    int dec_val_1 = 1 * std::pow(2, 3) + 1 * std::pow(2, 2) + 0 * std::pow(2, 1) + 1 * std::pow(2, 0);
    std::cout << "1. 1101 (Bin) = " << dec_val_1 << std::endl;

    // Переведення 101110 у десяткову систему
    int dec_val_2 = 1 * std::pow(2, 5) + 0 * std::pow(2, 4) + 1 * std::pow(2, 3) + 
                    1 * std::pow(2, 2) + 1 * std::pow(2, 1) + 0 * std::pow(2, 0);
    std::cout << "2. 101110 (Bin) = " << dec_val_2 << std::endl;

    std::cout << "\n=== Відповідь №4: Конвертація (Десятковий -> Двійковий) ===\n";
    
    // Тут просто виведення захардкоджених значень
    std::cout << "1. 15 (Dec) = 1111 (Bin)" << std::endl;
    std::cout << "2. 53 (Dec) = 110101 (Bin)" << std::endl;

    std::cout << "\n=== Відповідь №5: Пояснення небезпечних конструкцій ===\n";
    
    // Опис проблемних місць у C++
    std::cout << "1. int y = foo(++x, x); -> НЕВИЗНАЧЕНА ПОВЕДІНКА (UB). Порядок аргументів не гарантовано." << std::endl;

    std::cout << "2. int x = 7 / -2; -> До стандарту C++11 результат залежав від компілятора." << std::endl;

    std::cout << "3. int x = -5 % 2; -> До стандарту C++11 знак залишку був невизначений." << std::endl;

    std::cout << "4. float x = 0.1 + 0.1; -> ПРОБЛЕМА ТОЧНОСТІ. Float не можна порівнювати через ==." << std::endl;
    
    std::cout << "5. int x = 3 / 0; -> CRASH / UB. Ділення на нуль заборонено." << std::endl;
    
    return 0;
}